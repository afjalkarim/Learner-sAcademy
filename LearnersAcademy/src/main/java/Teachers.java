

public class Teachers {

	private int tid;
	private String tFirstName;
	private String tLastName;
	private String tDob;
	private String tAddress;
	private String tPhone;
	private String tDesignation;
	
	public int getTid() {
		return tid;
	}
	public void setTid(int tid) {
		this.tid = tid;
	}
	public String gettFirstName() {
		return tFirstName;
	}
	public void settFirstName(String tFirstName) {
		this.tFirstName = tFirstName;
	}
	public String gettLastName() {
		return tLastName;
	}
	public void settLastName(String tLastName) {
		this.tLastName = tLastName;
	}
	public String gettDob() {
		return tDob;
	}
	public void settDob(String tDob) {
		this.tDob = tDob;
	}
	public String gettAddress() {
		return tAddress;
	}
	public void settAddress(String tAddress) {
		this.tAddress = tAddress;
	}
	public String gettPhone() {
		return tPhone;
	}
	public void settPhone(String tPhone) {
		this.tPhone = tPhone;
	}
	public String gettDesignation() {
		return tDesignation;
	}
	public void settDesignation(String tDesignation) {
		this.tDesignation = tDesignation;
	}
	
	
	
}
